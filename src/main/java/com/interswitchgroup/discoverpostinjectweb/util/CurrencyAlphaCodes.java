    public static String getAlpha2Code(String countryCode,char currencyConversionIndicator)
    {
        String dp ="";

        try {

            if ('Y' == currencyConversionIndicator)
            {

                if (alphaCodes.containsValue(countryCode))
                {
                    dp = getKeysFromValue(alphaCodes, countryCode);
                }
            }
            else
            {
                dp = countryCode;
            }


        }
        catch (Exception ex) {
            // logger.error("getAlpha2Code() get Message exception:" + ex.getMessage());
            //LoggerUtil.logError(logger,ex);
        }

        return dp;
    }

    public static String getKeysFromValue(HashMap<?,?> hm, Object value){
        List<Object> list = new ArrayList<>();
        hm.keySet().stream().filter((o) -> (hm.get(o).equals(value))).forEach((o) -> {
            list.add(o);
        });
        return list.get(0).toString();
    }
